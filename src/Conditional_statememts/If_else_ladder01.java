package Conditional_statememts;

public class If_else_ladder01 {
	public static void main(String[] args) {
		int i=20;
		if(i<10) {
			System.out.println("i is less thsn 10");
		}
		else if(i<15) {
			System.out.println("i is less than 15");
		}
		else if(i<20) {
			System.out.println("i is less than 20");
		}
		else {
			System.out.println("i is greater than or equal to 20");
		}
	}

}
