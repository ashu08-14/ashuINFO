package Conditional_statememts;

public class If_else_ladder02 {
	public static void main(String[] args) {
		int i=30;
		if(i<10) {
			System.out.println("value of i is 10");
		}
		else if(i<20) {
			System.out.println("value of i is 20");
		}
		else if(i<30) {
			System.out.println("value of i is 30");
		}
		else if(i<40) {
			System.out.println("value of i is 40");
		}
		else {
			System.out.println("This is else statement");
		}

}
}