package Conditional_statememts;

public class Switch_Statement01 {
	public static void main(String[] args) {
		switch (30) {
		case 29:{
			System.out.println("number is in small size");
			break;
		}
		case 44:{
			System.out.println("number is medium size");
			break;
		}
		case 55:{
			System.out.println("number is large in size");
			break;
		}
		case 66:{
			System.out.println("number is extra large in size");
			break;
		}
		default:{
			System.out.println("number is unknown");
		}
		}
	}

}
